        $code = $this->generate(SerializedNameFixture::class);

        $this->assertMatchesRegularExpression("/extractString\\(\\\$data, 'email_address', required: true,/", $code);
    }

    public function testAliasedConstructorParameterInnerUsesNullableVariantWithRequiredFalse(): void
    {
        // The inner fallback call MUST use the Nullable variant so a
        // missing fallback key can propagate as `null` rather than the
        // non-null `""` sentinel (which would otherwise short-circuit the
        // outer call's required / default decision).
        //
        // It also MUST use `required: false` so the inner call itself
        // does not throw MissingRequiredFieldException when the fallback
        // key is missing — that decision belongs to the outer call, which
        // will re-evaluate it against the canonical key.
        $code = $this->generate(SerializedNameFixture::class);

        $this->assertMatchesRegularExpression(
            "/extractNullableString\\(\\\$data, 'emailAddress', required: false, default: null,/",
            $code,
        );
    }

    public function testAliasedOptionalParameterPreservesUserDefaultAtTheInnermostLayer(): void
