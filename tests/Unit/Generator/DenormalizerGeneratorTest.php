        // Both the canonical AND fallback keys must land in the skip map
        // so that when a fresh instance is built via construct(), the
        // populate() phase does not accidentally rewrite a constructor
        // field just because the fallback key happened to be the one
        // present in the payload.
        $code = $this->generate(SerializedNameFixture::class);

        // The skip map contains a ['email_address', 'emailAddress', ...] entry.
        $this->assertMatchesRegularExpression(
            "/array_fill_keys\\(\\[[^\\]]*'email_address'[^\\]]*'emailAddress'[^\\]]*\\]/",
            $code,
        );
    }

    public function testUnaliasedPopulatePhaseGuardUsesSingleKeyExistsCheck(): void
    {
        // PersonFixture has a nickname populate-phase field with NO alias,
        // so the populate() guard must stay compact — no OR-chain of
        // `array_key_exists` calls for fields that only have one key.
        $code = $this->generate(PersonFixture::class);

        // The `nickname` guard should be a plain single-key check.
        $this->assertMatchesRegularExpression(
            "/array_key_exists\\('nickname', \\\$data\\) && !isset\\(\\\$skip\\['nickname'\\]\\)/",
            $code,
        );
    }

    public function testUnaliasedPopulateExtractCallIsNotChained(): void
    {
        // The unaliased populate branch must emit a flat `extract*` call
        // without a nested inner call — the chained pattern is reserved
        // for fields that genuinely need the fallback.
        $code = $this->generate(PersonFixture::class);

        // The $nickname assignment must NOT contain a nested extract call
        // in its `default:` argument. We assert the compact shape directly.
        $this->assertMatchesRegularExpression(
            "/\\\$object->nickname = \\\$this->extractNullableString\\(\\\$data, 'nickname', required: false, default: null, context: \\\$context\\);/",
            $code,
        );
    }

    /**
     * Convenience helper that generates the denormalizer source for the given
     * target FQCN and returns the full PHP source string.
     *
     * @param class-string $targetFqcn
     */
    private function generate(string $targetFqcn): string
    {
        $metadata = $this->generator->getMetadataFactory()->getMetadataFor($targetFqcn);

        return $this->generator->generate($metadata);
    }
